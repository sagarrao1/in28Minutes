package com.learning.primitive_dataTypes;

public class Operators {

	public static void main(String[] args) {
		boolean isDone = true;
		System.out.println(isDone);

		int i = 12;

		boolean result = false;
		result = i >= 15 && i <= 25;
		System.out.println(result);

		result = i >= 15 || i <= 25;
		System.out.println(result);
	}

}
